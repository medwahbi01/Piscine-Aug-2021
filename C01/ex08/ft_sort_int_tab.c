#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	t;
	int	i;
	int	j;

	i = 1;
	while (i < size)
	{
		t = tab[i];
		j = i - 1;
		while (j >= 0 && tab[j] > t)
		{
			tab[j + 1] = tab[j];
			j = j - 1;
		}
		tab[j + 1] = t;
		i++;
	}
}
