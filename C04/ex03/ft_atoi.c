int	ft_atoi(char *str)
{
	int	i;
	int	resu;
	int	sign;

	sign = 1;
	i = 0;
	resu = 0;
	while (*str == '\t' || *str == '\n' || *str == '\v'
		|| *str == '\f' || *str == '\r' || *str == ' ')
		str++;
	while (*str == '+' || *str == '-')
		if (*(str++) == '-')
			sign *= -1;
	while (str[i] >= '0' && str[i] <= '9')
	{
		resu = resu * 10 + str[i] - '0';
		i++;
	}
	return (resu * sign);
}
