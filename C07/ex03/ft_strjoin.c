#include <stdlib.h>
#include <unistd.h>
#include <stdio.h>

char	*ft_strcat(char *dest, char *src)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (dest[i] != '\0')
		i++;
	while (src[j] != '\0')
	{
		dest[i + j] = src[j];
		j++;
	}
	dest[i + j] = '\0';
	return (dest);
}

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	return (i);
}

int	ft_total(char **strs, int size, char *sep)
{
	int	i;
	int	len;

	i = 0;
	len = 0;
	len = (size - 1) * ft_strlen(sep);
	while (i < size)
	{
		len = len + ft_strlen(strs[i]);
		i++;
	}
	return (len);
}

char	*ft_strjoin(int	size, char **strs, char *sep)
{
	char	*str;
	int		i;

	if (size == 0)
	{
		str = (char *)malloc(1);
		*str = 0;
		return (str);
	}
	str = (char *)malloc(1 * ft_total(strs, size, sep));
	if (str == NULL)
		return (NULL);
	*str = 0;
	i = 0;
	while (i < size)
	{
		ft_strcat(str, strs[i]);
		if (i < size - 1)
			ft_strcat(str, sep);
		i++;
	}
	return (str);
}
